package homeworks.medicalCenter.model;

import java.util.Objects;

public abstract class Person {
    protected String id;
    protected String name;
    protected String surname;
    protected String phone;
    protected String email;
    protected String doctor;

    public Person(String id, String name, String surname, String phone, String email) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.phone = phone;
        this.email = email;
        this.doctor = doctor;
    }

    public Person() {
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDoctor() {
        return doctor;
    }

    public void setDoctor(String doctor) {
        this.doctor = doctor;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Person person = (Person) o;
        return Objects.equals(id, person.id) && Objects.equals(name, person.name)
                && Objects.equals(surname, person.surname) && Objects.equals(phone, person.phone)
                && Objects.equals(email, person.email) && Objects.equals(doctor, person.doctor);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name, surname, phone, email, doctor);
    }

    @Override
    public String toString() {
        return "Person{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", surname='" + surname + '\'' +
                ", phone='" + phone + '\'' +
                ", email='" + email + '\'' +
                ", doctor='" + doctor + '\'' +
                '}';
    }
}
